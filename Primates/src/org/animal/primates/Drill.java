package org.animal.primates;

/**
 * Represents Drill species of the new world primate.
 */
public class Drill extends NewWorldPrimate {

  /**
   * Constructs an instance of primate belonging to the drill species.
   *
   * @param name          represents name of the primate of drill species.
   * @param sex           represents sex of the primate of drill species.
   * @param age           represents age of the primate of drill species.
   * @param weight        represents weight of the primate of drill species.
   * @param size          represents size of the primate of drill species.
   * @param favouriteFood represents favourite food of the drill species.
   */
  public Drill(String name, String sex, int age, double weight, double size, String favouriteFood) {
    super(name, sex, age, weight, size, favouriteFood);

  }

}
